function waitingTime(waitingTimes, serialNumber) {
  if (
    !Array.isArray(waitingTimes) ||
    waitingTime.length > serialNumber ||
    typeof serialNumber !== "number"
  ) {
    return "Invalid Input";
  }
  let totalTime = 0;
  for (let i of waitingTimes) {
    totalTime += i;
  }
  let avgTime = Math.round(totalTime / waitingTimes.length);
  let before = serialNumber - 1;
  //   console.log(before);
  let remaining = before - waitingTimes.length;
  //   console.log(remaining);
  return avgTime * remaining;
}
